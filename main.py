    def selectAppPath(self):
        dialog = QFileDialog.getOpenFileName(MainWindow)
        appPath = str(dialog)
        appPath = appPath.replace("(", "")
        appPath = appPath.replace(")", "")
        appPath = appPath.replace(", 'All Files *'", "")
        appPath = appPath.replace("'", "")
        appPath = appPath.replace(" ", "\\ ")
        self.appPath.setText(appPath)
        createinstallmedia = appPath + "/Contents/Resources/createinstallmedia"
        self.USBName_2.setText("sudo " + createinstallmedia)
